// NodeTemplate ‚Äî declarative component description
// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

/// Visual shape of a node body. Borrowed from Zeal's `NodeShape`.
/// `Rectangle` is the default; `Circle` / `Diamond` suit
/// connector-style or decision-point nodes. `Custom` lets a host
/// supply its own renderer via the editor's renderer-override API;
/// builds without that hook fall back to `Rectangle`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Default)]
pub enum NodeShape {
    #[default]
    Rectangle,
    Circle,
    Diamond,
    /// Host supplies its own renderer for this shape via an editor
    /// override. Reserved variant.
    Custom,
}

/// Declarative description of a component. The palette renders one
/// entry per template; instances render from the template's port +
/// metadata layout.
///
/// `component` is the host's identifier for the underlying actor /
/// function / etc. (e.g. reflow's `GraphNode.component`). Two
/// instances of the same template share the same `component` string.
///
/// `category` groups templates in the palette (e.g. "data",
/// "control", "media") ‚Äî host-defined, opaque to the editor.
#[derive(Clone)]
pub struct NodeTemplate<K: PortKind> {
    pub component: String,
    pub category: String,
    pub display_name: String,
    /// Subtitle the palette + node-body renderer surface beneath
    /// `display_name`. `None` = no subtitle. Equivalent to Zeal's
    /// `NodeMetadata.subtitle` (e.g. "Read Data", "Commit History").
    pub subtitle: Option<String>,
    pub inputs: Vec<PortDesc<K>>,
    pub outputs: Vec<PortDesc<K>>,
    /// Default shape for instances of this template. Per-instance
    /// override via [`NodeInstance::shape`].
    pub default_shape: NodeShape,
    /// Optional icon rendered in the node header. Construct via
    /// [`NodeIcon::from_svg_str`] from any SVG source ‚Äî tabler,
    /// lucide, Material, custom markup. The editor parses once at
    /// template build time and renders via
    /// `blinc_svg::SvgDocument::render_fit`, so the same SVG works
    /// inside the canvas closure (unlike text, which is currently
    /// blocked upstream).
    pub icon: Option<NodeIcon>,
    /// Default variant token for the renderer (Zeal-style semantic
    /// colour role ‚Äî `"primary"`, `"warning"`, `"surface"`, ‚Ä¶).
    /// `None` = use the theme's default. Per-instance override via
    /// [`NodeInstance::variant`].
    pub default_variant: Option<String>,
    /// Strongly-typed schema description of the node's config
    /// fields ‚Äî drives the inspector form. An empty schema means
    /// "no config" ‚Äî no inspector pane shown. Per-variant validation
    /// (`required`, `min`/`max`, select options, ‚Ä¶) lives on
    /// [`PropertyDefinition`](crate::config::PropertyDefinition);
    /// see [`crate::config::default_config`] /
    /// [`crate::config::validate`] for the host-side helpers.
    pub config_schema: ConfigSchema,
    /// Free-form template metadata ‚Äî version, doc URL, recommended
    /// backend (per-host concept), etc. The palette can surface a
    /// subset; the editor itself doesn't read it.
    pub metadata: serde_json::Value,
    /// Optional content slot rendered inside the node body via
    /// [`blinc_portal_ui::Portal`]. Adds `content.height` pixels
    /// beneath the header; the closure paints into that region every
    /// frame using immediate-mode widgets. See
    /// [`Self::with_content`] for the builder + the portal-ui crate
    /// README for the widget surface.
    pub content: Option<NodeContent>,
}

/// Metadata-driven node content slot. See
/// [`NodeTemplate::with_content`].
#[derive(Clone)]
pub struct NodeContent {
    /// Body region height in pixels ‚Äî added to the node's overall
    /// height. The portal paints inside a body rect of this height.
    pub height: f32,
    /// Per-frame UI closure. Receives the node id (for per-instance
    /// signal / state lookup) and a portal-ui builder; emit widgets
    /// or call `ui.allocate_painter(...)` for free-form painting.
    #[allow(clippy::type_complexity)]
    pub render: Arc<dyn Fn(&NodeId, &mut blinc_portal_ui::PortalUi) + Send + Sync>,
}

impl std::fmt::Debug for NodeContent {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("NodeContent")
            .field("height", &self.height)
            .field("render", &"<closure>")
            .finish()
    }
}

impl<K: PortKind> std::fmt::Debug for NodeTemplate<K>
where
    K: std::fmt::Debug,
{
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("NodeTemplate")
            .field("component", &self.component)
            .field("display_name", &self.display_name)
            .field("subtitle", &self.subtitle)
            .field("inputs", &self.inputs.len())
            .field("outputs", &self.outputs.len())
            .field("content", &self.content)
            .finish()
    }
}

impl<K: PortKind> NodeTemplate<K> {
    /// Minimal constructor ‚Äî no config, no metadata. Used in tests
    /// and for the simplest "I just want a node with these ports"
    /// case.
    pub fn new(component: impl Into<String>, display_name: impl Into<String>) -> Self {
        Self {
            component: component.into(),
            category: String::new(),
            display_name: display_name.into(),
            subtitle: None,
            inputs: Vec::new(),
            outputs: Vec::new(),
            default_shape: NodeShape::default(),
            icon: None,
            default_variant: None,
            config_schema: ConfigSchema::new(),
            metadata: serde_json::Value::Null,
            content: None,
        }
    }

    /// Attach an immediate-mode UI rendered inside the node body.
    ///
    /// `height` is the body region's height in pixels (the node
    /// grows by this much beneath the header). `render` runs every
    /// frame inside [`blinc_portal_ui::Portal::frame`] ‚Äî emit
    /// widgets (`ui.label`, `ui.slider_signal`, ...) or call
    /// `ui.allocate_painter(...)` for free-form painting (sparklines,
    /// sketch surfaces, custom visualisations).
    ///
    /// The closure receives the node id, useful for looking up
    /// per-instance state (signals keyed by node id, etc.). All
    /// portal state ‚Äî widget interaction state in `PortalStorage`,
    /// signal subscriptions ‚Äî is owned by the editor's per-node
    /// portal map.
    pub fn with_content<F>(mut self, height: f32, render: F) -> Self
    where
        F: Fn(&NodeId, &mut blinc_portal_ui::PortalUi) + Send + Sync + 'static,
    {
        self.content = Some(NodeContent {
            height,
            render: Arc::new(render),
        });
        self
    }

    pub fn with_category(mut self, category: impl Into<String>) -> Self {
        self.category = category.into();
        self
    }

    pub fn with_subtitle(mut self, subtitle: impl Into<String>) -> Self {
        self.subtitle = Some(subtitle.into());
        self
    }

    pub fn with_input(mut self, port: PortDesc<K>) -> Self {
        self.inputs.push(port);
        self
    }

    pub fn with_output(mut self, port: PortDesc<K>) -> Self {
        self.outputs.push(port);
        self
    }

    pub fn with_shape(mut self, shape: NodeShape) -> Self {
        self.default_shape = shape;
        self
    }

    pub fn with_icon(mut self, icon: NodeIcon) -> Self {
        self.icon = Some(icon);
        self
    }

    pub fn with_variant(mut self, variant: impl Into<String>) -> Self {
        self.default_variant = Some(variant.into());
        self
    }

    /// Set the typed config schema. Pass `vec![]` for "no config".
    pub fn with_config_schema(mut self, schema: ConfigSchema) -> Self {
        self.config_schema = schema;
        self
    }

    /// Append a single property to the schema. Convenience for
    /// builder chains that prefer one-property-per-line over
    /// constructing the whole `ConfigSchema` up front.
    pub fn with_property(mut self, property: impl Into<crate::config::PropertyDefinition>) -> Self {
        self.config_schema.properties.push(property.into());
        self
    }

    /// Append a reactive cascade rule to the schema.
    /// See [`crate::config::PropertyRule`].
    pub fn with_rule(mut self, rule: crate::config::PropertyRule) -> Self {
        self.config_schema.rules.push(rule);
        self
    }

    pub fn with_metadata(mut self, metadata: serde_json::Value) -> Self {
        self.metadata = metadata;
        self
    }
}

