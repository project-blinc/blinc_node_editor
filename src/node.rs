// NodeInstance ‚Äî a placed node in the editor's model
// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

/// A live node placed in the editor. References its template by
/// `component` name; the editor looks up the template at render time
/// to determine port positions and the inspector schema.
///
/// **Per-instance render overrides**: `shape`, `variant`, `icon`,
/// `subtitle` all default to `None` and fall back to the template's
/// defaults ‚Äî useful when most instances of a template render
/// identically but a few want their own variant (e.g. a single
/// `HttpFetch` template instantiated as both "normal" and "critical"
/// styling per use).
///
/// `M` is the host metadata type ‚Äî opaque to the editor. For nan8 +
/// reflow, this carries `GraphNode.metadata` (a `HashMap<String,
/// Value>` containing `dynasb.*` deploy state, `ExecutionSite`
/// backend, etc.). For other hosts, `M` can be `()`.
#[derive(Debug, Clone)]
pub struct NodeInstance<M> {
    pub id: NodeId,
    /// Template's `component` field. The editor uses this to look up
    /// the `NodeTemplate` at render time.
    pub component: String,
    /// Top-left corner in canvas-content coordinates. Updated as the
    /// user drags.
    pub position: Point,
    /// Logical size in content units. If `None`, the renderer uses
    /// the theme's default node size (per
    /// [`NodeEditorTheme`](crate::theme::NodeEditorTheme)).
    pub size: Option<(f32, f32)>,
    /// Per-instance shape override. `None` = template default.
    pub shape: Option<NodeShape>,
    /// Per-instance subtitle override. `None` = template subtitle.
    /// Useful for hosts that want to show e.g. the configured table
    /// name on a `PostgresQuery` instance rather than the generic
    /// template subtitle.
    pub subtitle: Option<String>,
    /// Per-instance theme variant token override. `None` = template
    /// default ‚Üí theme default. Mirrors Zeal's per-instance variant.
    pub variant: Option<String>,
    /// Per-instance icon override. `None` = template icon. Same
    /// construction as [`NodeTemplate::icon`].
    pub icon: Option<NodeIcon>,
    /// Status badge rendered in the node's corner (Zeal-style chip).
    /// `None` = no badge. Hosts populate this from runtime state:
    /// "running", "errored last invocation", "validation issue".
    pub badge: Option<StatusBadge>,
    /// Current values for the template's `config_schema`. Free-form;
    /// the inspector reads + writes this.
    pub config: serde_json::Value,
    /// Host metadata for this node. The editor never reads this; it's
    /// the host's payload (e.g. reflow's `GraphNode.metadata`).
    pub metadata: M,
    /// Soft-disable flag. Hosts set this to mark a node as inactive
    /// without removing it from the graph (skipped during execution,
    /// excluded from runtime metrics, etc.). The renderer dims the
    /// node's body / header / icon / title and downgrades every
    /// incident edge to a faded "pending" style so the dataflow
    /// visually breaks at the disabled node. Defaults to `false`.
    pub disabled: bool,
    /// When `Some`, this node is a *subgraph reference* ‚Äî a
    /// navigation entry-point into the named subgraph stored on the
    /// editor under this id. The renderer forces
    /// [`NodeShape::Diamond`] regardless of the template's
    /// `default_shape`, and a double-click emits
    /// [`crate::event::EditorEvent::SubgraphRequested`] for hosts to
    /// open / navigate into. Mirrors Zeal's `SubgraphNodeMetadata`
    /// pattern (`metadata.type === 'subgraph'`). `None` = regular
    /// node, no override.
    pub subgraph_ref: Option<crate::subgraph::SubgraphId>,
}

impl<M: Default> NodeInstance<M> {
    pub fn new(id: impl Into<NodeId>, component: impl Into<String>, position: Point) -> Self {
        Self {
            id: id.into(),
            component: component.into(),
            position,
            size: None,
            shape: None,
            subtitle: None,
            variant: None,
            icon: None,
            badge: None,
            config: serde_json::Value::Null,
            metadata: M::default(),
            disabled: false,
            subgraph_ref: None,
        }
    }
}

impl<M> NodeInstance<M> {
    pub fn with_subtitle(mut self, subtitle: impl Into<String>) -> Self {
        self.subtitle = Some(subtitle.into());
        self
    }

    pub fn with_shape(mut self, shape: NodeShape) -> Self {
        self.shape = Some(shape);
        self
    }

    pub fn with_variant(mut self, variant: impl Into<String>) -> Self {
        self.variant = Some(variant.into());
        self
    }

    pub fn with_icon(mut self, icon: NodeIcon) -> Self {
        self.icon = Some(icon);
        self
    }

    pub fn with_badge(mut self, badge: StatusBadge) -> Self {
        self.badge = Some(badge);
        self
    }

    pub fn with_size(mut self, width: f32, height: f32) -> Self {
        self.size = Some((width, height));
        self
    }

    /// Replace the host metadata. Useful when the host re-derives it
    /// from a backing store (reflow `GraphExport` re-loaded, etc.).
    pub fn with_metadata(mut self, metadata: M) -> Self {
        self.metadata = metadata;
        self
    }

    /// Apply a config patch. The editor doesn't merge ‚Äî it overwrites.
    /// Hosts that want merge semantics should merge before passing in.
    pub fn with_config(mut self, config: serde_json::Value) -> Self {
        self.config = config;
        self
    }

    /// Flag the node as soft-disabled ‚Äî see [`Self::disabled`].
    pub fn with_disabled(mut self, disabled: bool) -> Self {
        self.disabled = disabled;
        self
    }

    /// Mark this node as a subgraph reference ‚Äî see
    /// [`Self::subgraph_ref`]. The renderer will force the diamond
    /// shape and the editor emits
    /// [`crate::event::EditorEvent::SubgraphRequested`] on
    /// double-click.
    pub fn with_subgraph_ref(mut self, sub: impl Into<crate::subgraph::SubgraphId>) -> Self {
        self.subgraph_ref = Some(sub.into());
        self
    }
}
