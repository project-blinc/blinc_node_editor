//! Node types ‚Äî template (declarative) + instance (placed in editor).
//!
//! Per DIRECTIVE.md ¬ß4: a node is an INSTANCE of a TEMPLATE. The
//! template declares the component's ports + config schema; the
//! instance carries position, current config values, and host
//! metadata. The editor never hardcodes a node's fields ‚Äî it renders
//! any node by walking its template.

